namespace Werkraum\DeeplTranslate\DeepL;

class DeepLException extends \Exception
{

    /**
     * @var string
     */
    protected $detail = '';

    /**
     * @return string
     */
    public function getDetail()
    {
        return $this->detail;
    }

    /**
     * @param string $detail
     */
    public function setDetail($detail): void
    {
        $this->detail = $detail;
    }
}
